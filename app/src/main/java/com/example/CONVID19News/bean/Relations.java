package com.example.CONVID19News.bean;

public class Relations {
    private String relation;
    private String dt_url;
    private String dt_label;
    private String forward;

    public String getRelation() {
        return relation;
    }

    public void setRelation(String relation) {
        this.relation = relation;
    }

    public String getForward() {
        return forward;
    }

    public void setForward(String forward) {
        this.forward = forward;
    }

    public String getDt_label() {
        return dt_label;
    }

    public void setDt_label(String dt_label) {
        this.dt_label = dt_label;
    }

    public String getDt_url() {
        return dt_url;
    }

    public void setDt_url(String dt_url) {
        this.dt_url = dt_url;
    }

}

